def tuple_add(t1: tuple, t2: tuple) -> tuple:
    return t1[0] + t2[0], t1[1] + t2[1]


def tuple_mul(t1: tuple, t2: tuple) -> tuple:
    return t1[0] * t2[0], t1[1] * t2[1]


def tuple_div(t1: tuple, t2: tuple) -> tuple:
    return t1[0] / t2[0], t1[1] / t2[1]


def tuple_sub(t1: tuple, t2: tuple) -> tuple:
    return t1[0] - t2[0], t1[1] - t2[1]
